task autonomous () {
		/*motor[L] = -100;
		motor[R] = 100;
		wait1Msec(4000);
		motor[L] = 0;
		motor[R] = 0;*/
		//WIP AUTONOMOUS

		//Lift Tower
		motor[TowerL1] = 100;
		motor[TowerL2] = 100;
		motor[TowerR1] = 100;
		motor[TowerR2] = 100;
		wait1Msec(1000);
		motor[TowerL1] = 0;
		motor[TowerL2] = 0;
		motor[TowerR1] = 0;
		motor[TowerR2] = 0;

		//Close Claw partway
		motor[Claw] = -100;
		wait1Msec(400);
		motor[Claw] = 0;

		//Lower Tower
		motor[TowerL1] = -100;
		motor[TowerL2] = -100;
		motor[TowerR1] = -100;
		motor[TowerR2] = -100;
		wait1Msec(1000);
		motor[TowerL1] = 0;
		motor[TowerL2] = 0;
		motor[TowerR1] = 0;
		motor[TowerR2] = 0;

		//Close Claw fully
		motor[Claw] = -100;
		wait10Msec(600);
		motor[Claw] = 0;

		//Lift Tower
		motor[TowerL1] = 100;
		motor[TowerL2] = 100;
		motor[TowerR1] = 100;
		motor[TowerR2] = 100;
		wait1Msec(1000);
		motor[TowerL1] = 0;
		motor[TowerL2] = 0;
		motor[TowerR1] = 0;
		motor[TowerR2] = 0;

		//Move forward
		while(SensorValue[LEncoder] <= 500 && SensorValue[REncoder] <= 500){
			if(SensorValue[LEncoder] <= 500){
				motor[L] = 127;
			}
			else{
				motor[L] = 0;
			}
			if(SensorValue[REncoder] <= 500){
				motor[R] = 127;
			}
			else{
				motor[R] = 0;
			}
		}

		//Lower Tower Slightly
		motor[TowerL1] = -100;
		motor[TowerL2] = -100;
		motor[TowerR1] = -100;
		motor[TowerR2] = -100;
		wait1Msec(200);
		motor[TowerL1] = 0;
		motor[TowerL2] = 0;
		motor[TowerR1] = 0;
		motor[TowerR2] = 0;

		//Open Claw
		motor[Claw] = 100;
		wait10Msec(500);
		motor[Claw] = 0;
}
task usercontrol(){
	while (1==1){
		//Right joystick controls right wheels
		motor[R] = vexRT[Ch3]*(0.65);
		//Left joystick controls left wheels
		motor[L] = vexRT[Ch2]*(0.65);

		//Above, we mutiplied the above values by 0.5 to reduce the power going to the motors by 50% in order to reduce overheating

		//Tower controlled by buttons 5U and 5D
		if(vexRT[Btn5D]){
			motor[TowerL1] = -100;
			motor[TowerL2] = -100;
			motor[TowerR1] = -100;
			motor[TowerL2] = -100;
		}
		else if(vexRT[Btn5U]){
			motor[TowerL1] = 100;
			motor[TowerL2] = 100;
			motor[TowerR1] = 100;
			motor[TowerL2] = 100;
		}
		else{
			motor[TowerL1] = 0;
			motor[TowerL2] = 0;
			motor[TowerR1] = 0;
			motor[TowerL2] = 0;
		}
		//Claw opening and closing controlled by Buttons 7U and 8U
		if(vexRT[Btn7U]){
			motor[Claw] = 100;
		}
		else if(vexRT[Btn8U]){
			motor[Claw] = -100;
		}
		else{
			motor[Claw] = 0;
		}
		if(vexRT[Btn8D]){
			startTask(autonomous);
			wait1Msec(15000);
			stopTask(autonomous);
		}
		/* press the bottom button of the right buttons to
		start the autonomous*/
	}
}

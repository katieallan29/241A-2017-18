void towerCtrl(int power, int time){
		motor[TowerL1] = power;
		motor[TowerL2] = power;
		motor[TowerR1] = power;
		motor[TowerR2] = power;
		wait1Msec(time);
		motor[TowerL1] = 0;
		motor[TowerL2] = 0;
		motor[TowerR1] = 0;
		motor[TowerR2] = 0;
}
void clawCtrl(int power, int time, bool Stop){
		motor[Claw] = power;
		wait1Msec(time);
		if(Stop){
			motor[Claw] =  0;
		}
}
void move(int ticks){
	SensorValue[REncoder] = 0;
	SensorValue[LEncoder] = 0;
	while(SensorValue[LEncoder] <= ticks && SensorValue[REncoder] <= ticks){
		if(SensorValue[LEncoder] <= ticks){
			motor[L] = 127;
		}
		if(SensorValue[REncoder] <= ticks){
			motor[R] = 127;
		}
	}
	motor[L] = 0;
	motor[R] = 0;
}
void pre_auton (){
}
task autonomous () {
		//Lift Tower
		towerCtrl(100, 1000);

		//Close Claw partway
		clawCtrl(-100, 125, true);

		//Lower Tower
		towerCtrl(-63, 1000);

		//Close Claw fully
		clawCtrl(-127, 800, false);
		//NOT STOPPING CLAW TO ENSURE CONE REMAINS IN THE CLAW

		//Lift Tower
		towerCtrl(100, 1000);

		//Move forward
		move(200);

		//Lower Tower Slightly
		towerCtrl(-100, 200);

		//Open Claw
		clawCtrl(100, 500, true);
}
task usercontrol(){
	displayLCDPos(0, 0);
	displayNextLCDString("Battery:");
	displayLCDPos(0, 9);
	displayNextLCDNumber(nAvgBatteryLevel);
	while (1==1){
		//Right joystick controls right wheels
		motor[R] = vexRT[Ch3]*(0.65);
		//Left joystick controls left wheels
		motor[L] = vexRT[Ch2]*(0.65);

		//Above, we mutiplied the above values by 0.5 to reduce the power going to the motors by 50% in order to reduce overheating

		//Tower controlled by buttons 5U and 5D
		if(vexRT[Btn5D]){
			motor[TowerL1] = -100;
			motor[TowerL2] = -100;
			motor[TowerR1] = -100;
			motor[TowerL2] = -100;
		}
		else if(vexRT[Btn5U]){
			motor[TowerL1] = 100;
			motor[TowerL2] = 100;
			motor[TowerR1] = 100;
			motor[TowerL2] = 100;
		}
		else{
			motor[TowerL1] = 0;
			motor[TowerL2] = 0;
			motor[TowerR1] = 0;
			motor[TowerL2] = 0;
		}
		//Claw opening and closing controlled by Buttons 7U and 8U
		if(vexRT[Btn7U]){
			motor[Claw] = 100;
		}
		else if(vexRT[Btn8U]){
			motor[Claw] = -100;
		}
		else{
			motor[Claw] = 0;
		}
		if(vexRT[Btn8D]){
			startTask(autonomous);
			wait1Msec(15000);
			stopTask(autonomous);
		}
		/* press the bottom button of the right buttons to
		start the autonomous*/
	}
}
